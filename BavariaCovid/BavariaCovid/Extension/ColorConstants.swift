import UIKit

extension UIColor {
    static var kDarkRed: UIColor {
        return UIColor(red: 139, green: 0, blue: 0, alpha: 0.75)
    }
    
    static var kRed: UIColor {
        return UIColor(red: 255, green: 0, blue: 0, alpha: 0.5)
    }
    
    static var kYellow: UIColor {
        return UIColor(red: 255, green: 255, blue: 0, alpha: 0.5)
    }
    
    static var kGreen: UIColor {
        return UIColor(red: 0, green: 255, blue: 0, alpha: 0.5)
    }
}
